if is_locked:
    st.warning("⚠️ 此风险评估已确认，字段只读。如需修改，请先降级为 Draft。")

st.subheader("风险详情编辑器")
st.text_area("需求原文（只读）", value=selected_requirement["raw_text"], height=120, disabled=True)
