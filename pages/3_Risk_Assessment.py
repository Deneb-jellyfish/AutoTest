    if st.button("确认强制重新生成风险评估", key="force_regen_risk"):
        generate_or_update_risk(ps, selected_requirement, selected_parsed, use_llm, llm_client)
        save_state(ps)
        st.session_state["confirm_force_regen_risk"] = False
        st.success("已强制覆盖风险评估")
        st.rerun()

