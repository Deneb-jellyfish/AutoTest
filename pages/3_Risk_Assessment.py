def render_risk_matrix(risk_items: List[Dict[str, Any]]) -> str:
    matrix = {(impact, likelihood): [] for impact in range(1, 6) for likelihood in range(1, 6)}
    for item in risk_items:
        key = (int(item.get("impact", 1)), int(item.get("likelihood", 1)))
        matrix[key].append(item.get("req_id", ""))

    html = ['<table style="border-collapse:collapse;width:100%;font-size:13px;">']
    html.append("<tr><th style='padding:6px;border:1px solid #ddd;'>Impact \\ Likelihood</th>")
    for likelihood in range(1, 6):
        html.append(f"<th style='padding:6px;border:1px solid #ddd;'>{likelihood}</th>")
    html.append("</tr>")

    for impact in range(5, 0, -1):
        html.append(f"<tr><th style='padding:6px;border:1px solid #ddd;'>{impact}</th>")
        for likelihood in range(1, 6):
            score = impact * likelihood
            if score >= 12:
                bg = "#FECACA"
            elif score >= 6:
                bg = "#FEF3C7"
            else:
                bg = "#DCFCE7"
            reqs = "<br/>".join(matrix[(impact, likelihood)]) or "&nbsp;"
            html.append(
                f"<td style='vertical-align:top;padding:6px;border:1px solid #ddd;background:{bg};min-width:80px;'>{reqs}</td>"
            )
        html.append("</tr>")
    html.append("</table>")
    return "".join(html)


def generate_or_update_risk(ps: Dict[str, Any], requirement: Dict[str, Any], parsed_req: Dict[str, Any], use_llm: bool, llm_client: LlmClient) -> str:
    existing = find_risk_by_req_id(ps, requirement["req_id"])
    record = build_risk_item(
        requirement,
        parsed_req,
        risk_id=existing["risk_id"] if existing else generate_risk_id(ps),
        use_llm=use_llm,
        llm_client=llm_client,
        review_status="Draft",
        last_edited_by="llm",
    )
    if existing:
        old_record = deepcopy(existing)
        existing.update(record)
        log_change(ps, "risk", existing["risk_id"], "Regenerated", "record", old_record, existing, "llm", "Regenerated by risk page")
        return existing["risk_id"]

    ps["risk_items"].append(record)
    log_change(ps, "risk", record["risk_id"], "Created", "record", None, record, "llm", "Initial risk generation")
    return record["risk_id"]


